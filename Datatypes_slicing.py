#Data Types and Slicing

#Creating a list named mixed_data
mixed_data = [1, 'apple', 3.14, 'banana', 5, 6.7, 'orange', 8]

print("Original List:", mixed_data)

#Implementing  slicing

print("First Three Elements:", mixed_data[:3])

print("Elements from 2nd to 5th Position:", mixed_data[1:5])

print("Last Two Elements:", mixed_data[-2:])

